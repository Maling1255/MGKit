+ (NSString *)mg_deviceOperator
{
    CTTelephonyNetworkInfo *telephonyInfo = [[CTTelephonyNetworkInfo alloc] init];
    CTCarrier *carri = [telephonyInfo subscriberCellularProvider];
    return [carri carrierName];
}

