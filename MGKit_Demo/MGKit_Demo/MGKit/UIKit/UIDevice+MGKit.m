+ (NSString *)mg_UUID
{
//    return [[[UIDevice currentDevice] identifierForVendor] UUIDString];
    // 当你升级或者重装软件的时候，UUID会发生变化，这时候我们再去获取UUID时，得到的值和以前的不一样，但其实还是同一台设备
    // keychain里保存的信息不会因App被删除而丢失，在用户重新安装App后依然有效，数据还在。
    NSString * strUUID = (NSString *)[MGKeyChain mg_loadForKey: @"mg_uuid"];
    if ([strUUID isEqualToString: @""] || !strUUID) {
        // 生成uuid
        CFUUIDRef uuidRef = CFUUIDCreate(kCFAllocatorDefault);
        strUUID = (NSString *)CFBridgingRelease(CFUUIDCreateString (kCFAllocatorDefault,uuidRef));
        [MGKeyChain mg_saveData:strUUID forKey:@"mg_uuid"];
    }
    return strUUID;
}

